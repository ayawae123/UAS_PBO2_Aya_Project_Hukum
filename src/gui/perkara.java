    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris=jTable1.getSelectedRow();

        jTextField1.setText(jTable1.getValueAt(baris, 0).toString());
        jTextField2.setText(jTable1.getValueAt(baris, 1).toString());
        jTextField3.setText(jTable1.getValueAt(baris, 2).toString());        
        jTextField4.setText(jTable1.getValueAt(baris, 3).toString());        
        jTextField5.setText(jTable1.getValueAt(baris, 4).toString());        

        
        String id = jTable1.getValueAt(baris, 0).toString();
        String nama = jTable1.getValueAt(baris, 1).toString();
        String waktu = jTable1.getValueAt(baris, 2).toString();
        String lokasi = jTable1.getValueAt(baris, 3).toString();
        String nama_pelapor = jTable1.getValueAt(baris, 4).toString();

        // Mengisi field input dengan data yang diambil
        jTextField1.setText(id);
        jTextField2.setText(nama);
        jTextField3.setText(waktu);
        jTextField4.setText(lokasi);
        jTextField5.setText(nama_pelapor);
    }//GEN-LAST:event_jTable1MouseClicked

