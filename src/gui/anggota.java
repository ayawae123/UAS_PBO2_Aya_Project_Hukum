    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        int baris=jTable3.getSelectedRow();

        idAnggota.setText(jTable3.getValueAt(baris, 0).toString());
        nama.setText(jTable3.getValueAt(baris, 1).toString());
        jabatan.setText(jTable3.getValueAt(baris, 2).toString());        
        
        String id = jTable3.getValueAt(baris, 0).toString();
        String nm = jTable3.getValueAt(baris, 1).toString();
        String jab = jTable3.getValueAt(baris, 2).toString();
        
        // Mengisi field input dengan data yang diambil
        idAnggota.setText(id);
        nama.setText(nm);
        jabatan.setText(jab);
        
    }//GEN-LAST:event_jTable3MouseClicked

